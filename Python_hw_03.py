def int_func(word):
	""""Функция принимает слово из маленьких латинских букв и возвращает его же с большой первой буквой"""
	return word.capitalize()


def super_int_func(my_string):
	"""Функция на основе int_func, принимает строку слов из маленьких латинских букв и возвращает её же с каждым словом
	 начинающимся большой первой буквой"""
	s_list = my_string.split()
	for i in range(len(s_list)):
		s_list[i] = int_func(s_list[i])
