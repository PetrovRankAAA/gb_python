def my_func(x, y):
	""""Функция принимает два значения и возводит первый аргумент в степень второго"""
	number = 1.0
	x = int(x)
	power = int(y)
	if power > 0:
		for i in range(power):
			number *= x
		return number
	elif power < 0:
		for i in range(abs(power)):
			number /= x
		return number
	elif power == 0:
		return 1


